    }

    public function getUserIdentifier(): string
    {
        // TODO: Implement getUserIdentifier() method.
        return  $this->nombre;
    }

    public function getPassword(): ?string
    {
        // TODO: Implement getPassword() method.
        return  $this->contrasenia;
    }
}
